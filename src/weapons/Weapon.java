package weapons;

public abstract class Weapon {

     String name;
     int damage;
     int range;
     int speed;


    public  String getName() {
        return name;
    }



    public  int getDamage() {
        return damage;
    }



    public  int getRange() {
        return range;
    }



    public  int getSpeed() {
        return speed;
    }




}
